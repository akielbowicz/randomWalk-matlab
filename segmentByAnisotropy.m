function [segments, varargout] = segmentByAnisotropy(randomWalk,varargin)
% SEGMENTBYANISOTROPY encuentra los segmentos dentro de una caminata
% basandose en los valores de anisotropia local.
% S = SEGMENTBYANISOTROPY(WALK) es una celda con la cantidad de pasos de
% los segmentos y el indice temporal en el que empiezan dentro de la caminata.
% SEGMENTBYANISOTROPY(_,'Drift') indica que buscamos los segmentos con
% anisotropia menor a 0.03. Es la opcion predeterminada
% SEGMENTBYANISOTROPY(_,'Confined') indica que buscamos los segmentos con
% anisotropia mayor a 0.8
% SEGMENTBYANISOTROPY(_,'MinWindow',W) donde W Tiene que ser un valor impar.
% Define el tamano minimo de la ventana/puntos en el segmento.  El valor
% predeterminado es 11
% SEGMENTBYANISOTROPY(_,'Plot') genera una figura en la que se van
% mostrando los segmentos de distintos numeros de pasos
% SEGMENTBYANISOTROPY(_,'Histogram') genera una figura en la que se grafica
% un histograma del numero de segmentos en funcion del numero de pasos del
% segmento
% SEGMENTBYANISOTROPY(_,'Map') genera una figura en la que se grafica
% el mapa de temperatura y calcula unicamente los segmentos como
% componentes conexas del mapa, es decir de las islas
% [S,HIST] = SEGMENTBYANISOTROPY(_,'Histogram')
% [S,HIST,MAP] = SEGMENTBYANISOTROPY(_,'Histogram','Map')
% [S,HIST,MAP] = SEGMENTBYANISOTROPY(_,'Map')

%Set defaults

%bug 1 colores del mapa cuando el valor es 0

thresholdTop = 0.8;
thresholdBottom = 0.03;
type = 'Drift';
minWindow = 11;
plt = false;
htg = false;
map = true;
N = length(randomWalk);
mapImage = nan(N,N);
h = [];

for i=1:numel(varargin)
    if ischar(varargin{i})
        switch varargin{i}
            case 'Confined' %asignamos
                type = 'Confined';
                continue
            case 'Drift' %asignamos
                type = 'Drift';
                continue
            case 'MinWindow' %asignamos
                minWindow = varargin{i+1};
                continue
            case 'Plot'
                plt = true;
                continue
            case 'Histogram'
                htg = true;
                continue
            case 'Map'
                map = varargin{i+1};
                N = length(randomWalk);
                mapImage = nan(N,N);
                continue
            otherwise
                error('No valid input.')
        end
    end
    
end %i varargin

%%

fooSegment = cell(1,2);

fooSegment{1,1} = minWindow;

for positionIndex = 1+(minWindow-1)/2:length(randomWalk)-(minWindow-1)/2
    
    rangeIndex = minWindow;
    
    while and(positionIndex+(rangeIndex-1)/2 < length(randomWalk), ...
            positionIndex-(rangeIndex-1)/2 > 0)
        window = -(rangeIndex-1)/2:(rangeIndex-1)/2;
        eigenValues = eig(cov(randomWalk(positionIndex+window,:)));
        anisotropy = min(eigenValues)/max(eigenValues);
        
        foo = true;
        
        if map
            mapImage(positionIndex,rangeIndex) = anisotropy;
        end
        
        if (anisotropy < thresholdBottom) && strcmpi(type,'Drift')
            
        elseif (anisotropy > thresholdTop) && strcmpi(type,'Conf')
            
        else
            foo = false;
        end
        
        
        if foo && not(map)
            ind = ([fooSegment{:,1}] == rangeIndex);
            
            if any(ind)
                fooSegment{ind,2} = [fooSegment{ind,2} positionIndex];
            else
                fooSegment{end+1,1} = rangeIndex;
                fooSegment{end,2} = positionIndex;
            end
            
        end
        rangeIndex = rangeIndex+2;
    end %j n_rang
end %i numel caminata

% Calculo del mapa y las componentes conexas
if map % se grafica el mapa
    
    mapFix = mapImage(:,3:2:end);
    
    if strcmpi(type,'Drift')
        m = and(mapFix < thresholdBottom,0 < mapFix);
    elseif strcmpi(type,'Confined')
        m = mapFix > thresholdTop;
    end
    
    cc = bwconncomp(m, 4); % calculo de las componentes conexas/islas
    
    cluster = find(cellfun(@numel,cc.PixelIdxList)>5);
    
    for i = 1:numel(cluster)
        
        [I,J] = ind2sub(size(m),cc.PixelIdxList{cluster(i)});
        Indexes = zeros(sum(2*J+1),1);
        k = 1;
        for j = 1:numel(I)
            rangeIndex = J(j);
            window = -rangeIndex:rangeIndex;
            positionIndex = I(j);
            
            Indexes(k+(0:numel(window)-1)) = positionIndex+window;
            k = k+ numel(window);
        end
        
        
        ind = ([fooSegment{:,1}] == range(Indexes));
        if any(ind)
            fooSegment{ind,2} = [fooSegment{ind,2} min(Indexes)];
        else
            fooSegment{end+1,1} = range(Indexes);
            fooSegment{end,2} = min(Indexes);
        end
    end
    
end %if map

% Se ordenan los segmentos
ind1 = cellfun(@numel,fooSegment(:,2))>0;
fooSegment2 = fooSegment(ind1,:);
[ ~ , ind2] = sort([fooSegment2{:,1}]);

segments = fooSegment2(ind2,:);
%%
try
    if plt % si plot
        figure('Units', 'in',...
            'PaperSize',[5 5],...
            'Resize', 'off',...%             'Color', 'none',...
            'Position', [1 1 5 5])
        if ~map
            for i=1:numel([segments{:,1}])
                clf
                plot(randomWalk(:,1),randomWalk(:,2),'.-')
                hold on
                for j =1:numel(segments{i,2})
                    rangeIndex = segments{i,1};
                    window = -(rangeIndex-1)/2:(rangeIndex-1)/2;
                 
                    plot(randomWalk(segments{i,2}(j)+window-1,1),...
                        randomWalk(segments{i,2}(j)+window-1,2),...
                        '.-r','LineWidth',2)
                    
                    plot(randomWalk(segments{i,2}(j),1),...
                        randomWalk(segments{i,2}(j),2),'*k',...
                        'MarkerSize',4)
                    
                end
                title(sprintf('Segmentos de %d pasos',segments{i,1}));
                xlabel('x ( nm )');
                ylabel('y ( nm )');
                legend('off');
                formatFigure()
                drawnow
                pause(1)
            end
        else
            for i=1:numel([segments{:,1}])
                clf
                plot(randomWalk(:,1),randomWalk(:,2),'.-')
                hold on
                for j =1:numel(segments{i,2})
                    rangeIndex = segments{i,1};
                    window = 1:rangeIndex;
                 
                  
                    plot(randomWalk(segments{i,2}(j)+window-1,1),...
                        randomWalk(segments{i,2}(j)+window-1,2),...
                        '.-r','LineWidth',2)
                    
                    plot(randomWalk(segments{i,2}(j),1),...
                        randomWalk(segments{i,2}(j),2),'*k',...
                        'MarkerSize',4)
                    
                end
                title(sprintf('Segmentos de %d pasos',segments{i,1}));
                xlabel('x ( nm )');
                ylabel('y ( nm )');
                legend('off');
                formatFigure()
                drawnow
                pause(1)
            end
        end
    end
    
    if htg % calculo histograma
        h = zeros(size(segments));
        h(:,1) = [segments{:,1}];
        for i = 1:length(segments)
            h(i,2) = numel(segments{i,2});
        end
        figure
        bar(h(:,1),h(:,2))
    end
    
    if map % Grafico mapa de temperatura
        figure('Units', 'in',...
            'PaperSize',[5 5],...
            'Resize', 'off',...%             'Color', 'none',...
            'Position', [1 1 5 5])
        colormap(cMap)
        image(1:size(mapImage,2),1:size(mapImage,1),mapImage(:,3:2:end),'CDataMapping','scaled','AlphaData',mapImage(:,3:2:end)>0)
        axis('xy')
        set(gca,'Clim',[0 1])
        title ('');
        xlabel('Ancho de ventana');
        ylabel('PosiciÃ³n de la ventana');
        legend( 'off');
        formatFigure()
        colorbar
    end
    
    if nargout > 1
        nOutput = 1;
        if htg % si  histograma
            varargout{nOutput} = h;
            nOutput = nOutput + 1;
        elseif map % si  mapa
            varargout{nOutput} = mapImage;
        else % en caso contrario se devuelve histograma
            h = zeros(size(segments));
            h(:,1) = [segments{:,1}];
            for i = 1:length(segments)
                h(i,2) = numel(segments{i,2});
            end
            varargout{nOutput} = h;
            nOutput = nOutput + 1;
        end
    end
catch
    %
    if nargout > 1 % se devuelve el histograma
        if htg
            varargout{1} = h;
        else
            disp('Nothing to return')
        end
    end
end
end


